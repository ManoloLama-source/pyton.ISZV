# Pedir al usuario que introduzca una frase
frase = input("Introduce una frase: ")

# Invertir la frase
frase_invertida = frase[::-1]

# Mostrar la frase invertida
print("Frase invertida:", frase_invertida)

#[::] significa "desde el inicio hasta el final"

#[::-1] significa "desde el inicio hasta el final con paso -1" (invertido)

